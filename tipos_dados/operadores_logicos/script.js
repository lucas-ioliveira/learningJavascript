/*
&& ou and : E -> Para ser true os dois operandos precisam ser true
|| ou or : OU -> Para ser true pelo menos um dos operandos precisa ser true
! ou not : NÃO -> Para ser false o operando precisa ser true
*/

// ex:

console.log(true && true);
console.log(true && false);
console.log(false || false);
console.log(!false);



// Nesse caso o retorno é true ou false, tipo boolean